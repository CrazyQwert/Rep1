package learning;

public class UnitConversion {

	public static void main(String[] args) {
		char a = 'A';
		char c = 'C';
		int b = (int)a;
		System.out.println(b);
		
		int x = a * c;
		
		char alphabet = 'A';
		
		alphabet++;
		
		System.out.println(alphabet);
		
		int zahl = 123;
		
		
		String s = String.valueOf(zahl);
		
		System.out.println(s.length());
		
	}
}
